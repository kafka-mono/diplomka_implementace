from json import dumps


## From the class Logs object for every website is created. One object contains logs from browsers
#  with and without JShelter
#  when the same page is loaded.
class Logs:
    site = ''
    logs_without_addon = []
    logs_with_addon = []

    def __init__(self, site, logs_without_addon, logs_with_addon):
        self.site = site
        self.logs_without_addon = logs_without_addon
        self.logs_with_addon = logs_with_addon

    def to_json(self):
        return '{"site": "' + self.site + '", "logs_without_addon": ' + dumps(self.logs_without_addon) + ', "logs_with_addon": ' + dumps(self.logs_with_addon) + '}'
